<p>Urbanization, sustainability, digitization… The challenges facing mobility today are massive, and no company can hope to face them alone. The answer is to look for solutions together, which is why in 1998, Michelin started what was originally called The Bibendum Challenge in the tire manufacturer’s hometown of Clermont-Ferrand, in central France.</p>
